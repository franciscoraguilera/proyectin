    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
               
        String sql="select * from cliente where cod_cliente= "+ txtCod.getText();
        ResultSet rs=sen.consultas(sql);//recibimos la consulta en una variable ResultSet
        try {
            while(rs.next()) { //mientras hay datos en rs
                //cargamos en los campos el resultado de rs, getString obtiene el valor de
                //la base de datos segun la columna numerica.
                txtRuc.setText(rs.getString(3));
                txtNom.setText(rs.getString(2));
                txtDire.setText(rs.getString(4));
                txtTel.setText(rs.getString(5));
                txtCel.setText(rs.getString(6));
                txtRef.setText(rs.getString(7));
                txtCorreo.setText(rs.getString(8));
                txtNotas.setText(rs.getString(9));

            }

            btnNuevo.setEnabled(false);
            txtRuc.setEnabled(true);
            txtRuc.requestFocus();
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(GestionClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        conf.habTexto(new Object[]{txtRuc,txtNom,txtDire,txtTel,txtCel,txtCorreo,txtNotas,txtRef});
        conf.habBoton(new Object[]{btnGrabar});
        conf.desBoton(new Object[]{btnEliminar});
    }//GEN-LAST:event_btnModificarActionPerformed

