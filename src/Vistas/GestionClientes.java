  public void buscarElementos() throws SQLException {
   javax.swing.table.DefaultTableModel dt=(DefaultTableModel)tablaCliente.getModel();
   int cantidadfila= dt.getRowCount();        
        for( int i=cantidadfila-1; i>=0; i--) 
              dt.removeRow(i);
     try {
         String sql="SELECT * FROM "+tabla+" where "+campo+ " LIKE '%"+txtBuscarCliente.getText()+"%'";
         System.out.println(sql);
         ResultSet rsa=sen.consultas(sql);
        String datos[]=new String[cantidad];
      while(rsa.next()){
    for(int i=0; i<cantidad;i++){
        datos[i]=rsa.getString(dato[i]);
    }
    dt.addRow(datos);
}
    tablaCliente.setModel(dt);
     } catch (SQLException ex) {
         Logger.getLogger(GestionClientes.class.getName()).log(Level.SEVERE, null, ex);
     }
        
  }
