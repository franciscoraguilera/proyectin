private void grabar() {
            if(agrega) {
                if(sen.ejecutarInser("insert into proveedor values("+ txtCod.getText()+","
                         + "'" + txtNom.getText()+"',"
                        + "'" + txtDire.getText()+"',"
                        + "" + txtTel.getText()+","
                        + "" + txtCel.getText()+","
                        + "'" + txtContacto.getText()+"',"
                        + "'" + txtCorreo.getText()+"',"
                        + "'" + txtRuc.getText()+"'"
                        + ")"))
                
                conf.mensaje(3,"El sistema comunica", "Hecho");
                    
            }
             else {
                    sen.ejecutarModif("Update proveedor set ruc_proveedor='"+ txtRuc.getText()+"',"
                        + "nom_proveedor='" +txtNom.getText()+"',"
                        + "dire_proveedor='" +txtDire.getText()+"',"
                        + "tel_proveedor=" +txtTel.getText()+","
                        + "cel_proveedor=" +txtCel.getText()+","
                        + "contacto_proveedor='" +txtContacto.getText()+"',"
                        + "correo_proveedor='" +txtCorreo.getText()+"'"
                        + " where cod_proveedor= "+txtCod.getText()+"");
                
            }
                Actualizar();
                conf.limpiarTexto(new Object[]{txtCod,txtRuc,txtNom,txtDire,txtTel,txtCel,txtCorreo,txtContacto});
    }
