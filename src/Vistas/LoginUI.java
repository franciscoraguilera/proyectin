    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        if (etiUsuario.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un usuario");
            etiUsuario.requestFocus();
            return;
        }

        if (etiPass.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar una contraseña");
            etiPass.requestFocus();
            return;
        }
        
         try {
        IngresarSistema();
    } catch (SQLException ex) {
        Logger.getLogger(LoginUX.class.getName()).log(Level.SEVERE, null, ex);
    }
            
        

    }//GEN-LAST:event_btnIngresarActionPerformed

