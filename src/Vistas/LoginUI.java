        private void IngresarSistema() throws SQLException  {
        String sql="SELECT usuario,password FROM acceso WHERE "+"usuario='"+etiUsuario.getText()
                 +"'AND password='"+etiPass.getText()+"'";
         ResultSet rs=sen.consultas(sql);
         if(!rs.next()) {
             conf.mensaje(3,"USUARIO INCORRECTO","VUELVA A INTENTAR");
             conf.limpiarTexto(new Object[]{etiPass,etiUsuario});
             etiUsuario.requestFocus();
             
    } else {
            conf.mensaje(3,"USUARIO CORRECTO","CONECTADO");
            InterfazPrincipal i=new InterfazPrincipal();
            i.setVisible(true);
            this.dispose();
            
         }
    }
        

